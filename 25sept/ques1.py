class calculator:
    def addition(self,num1,num2):
        return num1+num2
    def difference(self,num1,num2):
        return num1-num2
    def multiplication(self,num1,num2):
        return num1*num2
    def division(self,num1,num2):
        return num1/num2
c1=calculator()
print(c1.addition(6,4))
print(c1.difference(6,4))
print(c1.multiplication(6,4))
print(c1.division(6,4))
   